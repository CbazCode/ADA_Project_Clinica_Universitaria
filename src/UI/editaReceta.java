    public void recibeDatos(Receta receta){
        CodigoAnterior = receta.getCodigoCit();
        tCodigo.setText(String.valueOf(receta.getCodigoCit()));
        jMedicamentos.setText(receta.getMedicamentos().trim().replaceAll("\\s[*]","\n *" ));
        jInstrucciones.setText(receta.getInstrucciones().trim().replaceAll("\\s[*]","\n *" ));      
    }
    
    public void modificar(){
        System.out.println("Modificar");
        File fileNuevo = new File("src/archivos/recetastemp.txt");
        File fileAntiguo = new File("src/archivos/recetas.txt");
        
        
        BufferedReader br;
   
        FileWriter fichero = null;
        PrintWriter pw = null;

        
        try{
            fichero = new FileWriter(fileNuevo.getAbsoluteFile(), true);
            pw = new PrintWriter(fichero);
            FileReader fr =new FileReader(fileAntiguo);
            br = new BufferedReader(fr);
            if(fileAntiguo.exists()){
                Object[] linea = br.lines().toArray();
                for(int i = 0; i<linea.length;i++){
                    String[] row = linea[i].toString().split("  ");
                    if(row[0].equals(String.valueOf(CodigoAnterior))){
                         pw.println(tCodigo.getText() + "  " + jMedicamentos.getText().trim().replaceAll("\\s{1,10}"," " )+ "  "+ jInstrucciones.getText().trim().replaceAll("\\s{1,10}"," " ));
                    }else{
                        pw.println(linea[i]);
                    }
                }  
                
                fr.close();
                br.close();
            } 
            
            pw.close();
            fichero.close(); 
            fileAntiguo.delete();
            //fileAntiguo.renameTo(fileNuevo);
            
            //fileNuevo.delete();
            
            
            
             
        }catch(Exception e){
                e.printStackTrace();
        }finally{
            try {
              if (null != fichero){  
                pw.close();
                fichero.close(); 
                
              }
                  
            } catch (Exception e2) {
                   e2.printStackTrace();
             }
            dispose();
                
        }
    }
    
